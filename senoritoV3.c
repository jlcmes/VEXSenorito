task usercontrol()
{
  // User control code here, inside the loop

  while (true)
  {
		// SOURCE CODE: https://www.vexforum.com/index.php/12370-holonomic-drives-2-0-a-video-tutorial-by-cody/0

		// MOVEMENT
		// Y component, X component, Rotation
		motor[FL] = -C1LY - C1LX - C1RX;
		motor[FR] =  C1LY - C1LX - C1RX;
		motor[BR] =  C1LY + C1LX - C1RX;
		motor[BL] = -C1LY + C1LX - C1RX;

		// INTAKE
		if (vexRT[Btn6U] == 1)
			motor[intakeMotor] = -SPEED_MOTORS-40;//20
		else if (vexRT[Btn6D] == 1)
			motor[intakeMotor] = SPEED_MOTORS+40;//20
		else
			motor[intakeMotor] = 0;

		// LIFT
		if (vexRT[Btn5U] == 1)
			motor[liftMotor] = SUPER_SPEED_MOTORS; //SPEED_MOTORS+20;
		else if (vexRT[Btn5D] == 1)
			motor[liftMotor] = -SUPER_SPEED_MOTORS; //-SPEED_MOTORS;
		else
			motor[liftMotor] = 0;

		// DEPLOY SYSTEM
		if (vexRT[Btn8U] == 1)
			motor[deployMotor] = SUPER_SPEED_MOTORS; //SPEED_MOTORS+60;//40
		else if (vexRT[Btn8D] == 1)
			motor[deployMotor] = -SUPER_SPEED_MOTORS; //-SPEED_MOTORS-60;//-40
		else
			motor[deployMotor] = 0;

		// TEST AUTONOMOUS MODE
		if (vexRT[Btn7L] == 1)
			startTask(autonomous);

		// TEST BUTTON
		if (vexRT[Btn7R] == 1)
	{
	startMotor(upMotor, SPEED_MOTORS);
	startMotor(downMotor, -SPEED_MOTORS);
	}

		// GO FORWARD AND BACKWARDS
		if (vexRT[Btn7U] == 1)
			startTask(goForward);
		else if (vexRT[Btn7D] == 1)
			startTask(goBackwards);
		else
		{
			stopMotor(leftMotor);
			stopMotor(rightMotor);
		}

		// Motor values can only be updated every 20ms
		wait1Msec(20);
  }
}
