task shakeClaw()
{
	while (true)
	{
		startMotor(intakeMotor, SUPER_SPEED_MOTORS);
		waitInMilliseconds(1000);
		startMotor(intakeMotor, -SUPER_SPEED_MOTORS);
		waitInMilliseconds(1000);
	}
}

task autonomous()
{
  // ..........................................................................
	startMotor(leftMotor, SPEED_MOTORS);
  startMotor(rightMotor, -SPEED_MOTORS);
	startMotor(liftMotor, SUPER_SPEED_MOTORS);

  waitInMilliseconds(3000);

	stopMotor(leftMotor);
	stopMotor(rightMotor);
	stopMotor(liftMotor);

	startMotor(liftMotor, SUPER_SPEED_MOTORS);
  waitInMilliseconds(2000);
	stopMotor(liftMotor);

	// DEPENDING OF THE POSITION - TO THE LEFT OR TO THE RIGHT (FLIP SIGNS)
	startMotor(upMotor, SPEED_MOTORS);
	startMotor(downMotor, -SPEED_MOTORS);
	startTask(shakeClaw);
	waitInMilliseconds(6000);


}

task goForward()
{
  // ..........................................................................
	startMotor(leftMotor, SPEED_MOTORS);
  startMotor(rightMotor, -SPEED_MOTORS);
}

task goBackwards()
{
  // ..........................................................................
	startMotor(leftMotor, -SPEED_MOTORS);
  startMotor(rightMotor, SPEED_MOTORS);
}

